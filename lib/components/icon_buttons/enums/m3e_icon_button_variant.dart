/// The four icon button color variants defined by Material 3.
enum M3EIconButtonVariant {
  /// Transparent container, low emphasis.
  standard,

  /// Filled with the primary color, high emphasis.
  filled,

  /// Filled with the secondary container color, medium emphasis.
  tonal,

  /// Outlined with a transparent container, medium emphasis.
  outlined,
}

/// The selectable icon button shapes, morphed on press.
enum M3EIconButtonShape {
  round,
  square,
}

/// The expressive icon button sizes.
enum M3EIconButtonSize {
  extraSmall,
  small,
  medium,
  large,
  extraLarge,
}
