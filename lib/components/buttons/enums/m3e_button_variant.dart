/// The five common button color variants defined by Material 3.
///
/// Ordered by emphasis, from the highest ([filled]) to the lowest ([text]).
enum M3EButtonVariant {
  /// Tonal surface with a subtle shadow. Medium emphasis.
  elevated,

  /// High emphasis, filled with the primary color.
  filled,

  /// Medium emphasis, filled with the secondary container color.
  filledTonal,

  /// Medium emphasis, outlined with a transparent container.
  outlined,

  /// Low emphasis, text only with no container.
  text,
}
