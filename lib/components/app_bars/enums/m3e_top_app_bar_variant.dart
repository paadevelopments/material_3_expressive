/// The four top app bar layouts defined by Material 3.
enum M3ETopAppBarVariant {
  /// 64dp bar with a leading-aligned title.
  small,

  /// 64dp bar with a centre-aligned title.
  center,

  /// 112dp bar with the title on a second line.
  medium,

  /// 152dp bar with a large title on a second line.
  large,
}
